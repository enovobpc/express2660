<h2 class="m-t-0">Política de Cookies</h2>
<div class="spacer-15"></div>
<h4>O que são cookies</h4>
<p>
    Os cookies são pequenos ficheiros de texto que um site, ao ser visitado 
    pelo utilizador, coloca no seu computador ou no seu dispositivo móvel 
    através do navegador de internet. A colocação de cookies ajudará o site 
    a reconhecer o seu dispositivo na próxima vez que o utilizador o visita.
</p>

<h4>Tipos de cookies</h4>
<p>
    Os cookies podem dividir-se em dois tipos distintos:
</p>
<ul>
    <li><strong>Cookies de sessão</strong>: São cookies que permanecem no navegador do utilizador apenas durante a visita, desaparecendo assim que o utilizador sair do website.</li>
    <li><strong>Cookies persistentes</strong>: São cookies que permanecem no navegador do utilizador durante um determinado período de tempo depois de o utilizador sair do website, exceto se este os apagar.</li>
</ul>

<h4>Que tipo de Cookies utiliza esta página?</h4>
<ul>
    <li><strong>Cookies de uso interno</strong>: Esta página de internet utiliza Cookies de uso interno imprescindíveis para o habitual funcionamento desta aplicação, como por exemplo, aquelas que permitem a autenticação ou a manutenção da sessão do utilizador registado. A desativação destas Cookies impede o funcionamento de alguns serviços desta web.</li>
    <li><strong>Cookies de terceiros</strong>: Esta página de internet utiliza Cookies de terceiros como complemento externo de conteúdos. Estas Cookies não são do site que está a aceder.</li>
    <li><strong>Cookies Analíticas</strong>: Esta página de internet utiliza Cookies analíticas para a avaliação da atividade da web e para a elaboração de perfis de navegação dos utilizadores destes locais, com o objetivo de introduzir melhorias em função da análise dos dados de utilização obtidos pelos utilizadores do serviço.</li>
</ul>

<h4>Como desativar as Cookies nos principais navegadores?</h4>
<p>
    O uso de cookies pode ser bloqueado.
    Como explicámos os cookies ajudam-no a tirar o máximo proveito dos nossos sites. 
    Depois de autorizar o uso de cookies, o utilizador pode sempre desativar 
    parte ou a totalidade dos nossos cookies mediante a configuração das opções 
    do seu navegador internet. Caso as bloqueie, é possível que certos serviços 
    que delas necessitam não estejam disponíveis para utilização.
</p>

