<h4>Medidas de Segurança</h4>
<p>
    A {{ Setting::get('company_name') }}, implementa medidas de carácter técnico e organizativo necessárias à protecção dos 
    Dados Pessoais contra a respectiva destruição, acidental ou ilícita, a perda acidental, a alteração, a 
    difusão ou o acesso não autorizado, nomeadamente quando o tratamento implicar a sua transmissão por rede, 
    bem como contra qualquer outra forma de tratamento ilícito dos mesmos, devendo tais medidas ser cumpridas 
    pelos seus colaboradores.
    <br/>
    Estas medidas assegurarão, atendendo aos conhecimentos técnicos disponíveis e aos custos resultantes da sua 
    aplicação, um nível de segurança adequado em relação aos riscos que o Tratamento de Dados Pessoais apresenta 
    e à natureza dos Dados Pessoais a proteger.
    <br/>
    A {{ Setting::get('company_name') }}, adoptará medidas especiais de segurança aquando do tratamento de Dados Sensíveis.
</p>

