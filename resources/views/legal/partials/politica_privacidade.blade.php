<h3>PRINCÍPIOS APLICÁVEIS AO TRATAMENTO DE DADOS PESSOAIS</h3>
<h4>Princípios Gerais</h4>
<p>
    O presente Tratamento de Dados Pessoais regula a recolha e tratamento dos seus Dados Pessoais, assim como o exercício
    dos seus direitos relativamente aos mesmos, nos termos da Legislação relativa à protecção de Dados Pessoais.
    <br/>
    Os Tratamentos de Dados Pessoais serão realizados de acordo com os seguintes princípios:
</p>
<ul class="list-unstyled">
    <li>(i) Licitude e lealdade;</li>
    <li>(ii) Finalidade;</li>
    <li>(iii) Adequação, pertinência e proporcionalidade;</li>
    <li>(iv) Exactidão;</li>
    <li>(v) Conservação;</li>
    <li>(vi) Informação;</li>
    <li>(vii) Segurança; e</li>
    <li>(viii) Legitimidade.</li>
</ul>

<h4>Finalidade</h4>
<p>
    Os Dados Pessoais recolhidos serão tratados e armazenados informaticamente pela {{ Setting::get('company_name') }}, ou pelas
    entidades por esta subcontratadas, para efeitos de gestão da relação entre a {{ Setting::get('company_name') }} e o Titular dos
    Dados com a finalidade de execução de serviços.
</p>

<h4>Recolha, tratamento e conservação de Dados Pessoais</h4>
<p>
    A {{ Setting::get('company_name') }}, só procederá ao tratamento de Dados Pessoais se os mesmos forem necessários para a
    prossecução da sua actividade, cumprimento de obrigações legais ou contratuais ou para a prossecução das finalidades indicadas acima.
    <br/>
    A {{ Setting::get('company_name') }}, assume que os dados recolhidos foram inseridos / disponibilizados pelo respectivo titular e
    que a sua inserção / disponibilização à {{ Setting::get('company_name') }}, foi autorizada pelo mesmo, sendo os Dados Pessoais
    em questão verdadeiros e exactos. Sem prejuízo, a {{ Setting::get('company_name') }} tomará as medidas adequadas para assegurar que
    sejam apagados ou rectificados os dados inexactos ou incompletos, tendo em conta as finalidades para que foram
    recolhidos ou para que são tratados posteriormente.
    <br/>
    Os Dados Pessoais recolhidos serão conservados de forma a permitir a sua identificação apenas durante o período
    necessário para a prossecução das finalidades de recolha ou do tratamento posterior, findo o qual os mesmos serão
    definitivamente eliminados, sem possibilidade de recuperação.
    <br/>
    Os Dados Pessoais que recolhemos incluem nomes, informações sobre o seu envio, moradas, endereços de e-mail e
    números de telefone. Algumas das informações recolhidas pela {{ Setting::get('company_name') }}, consistem em dados referentes a
    uma pessoa singular identificável.
</p>

<h4>Fluxos Transfronteiriços de Dados Pessoais</h4>
<p>
    A {{ Setting::get('company_name') }}, poderá proceder à transferência de Dados Pessoais para entidades situadas fora de Portugal,
    que concordem em utilizar tais Dados Pessoais para as finalidades indicadas nesta Política de Privacidade.
    <br/>
    A transferência de Dados Pessoais no âmbito desta, poderá envolver a transferência dos mesmos para países
    terceiros (fora do território da União Europeia / Espaço Económico Europeu) que poderão não dispor de leis e
    regras equivalentes em matéria de protecção de Dados Pessoais. Nestes casos, ser-lhe-á pedido o seu consentimento
    para tais transmissões.
</p>

<h4>Comunicação a Terceiros</h4>
<p>
    A {{ Setting::get('company_name') }} poderá comunicar Dados Pessoais a terceiros, no âmbito:
</p>
<ul>
    <li>Do cumprimento de uma obrigação legal, de uma deliberação da CNPD ou de uma ordem judicial;</li>
    <li>Quando a comunicação de dados seja levada a cabo para proteger interesses vitais do titular dos Dados Pessoais;</li>
    <li>No âmbito de uma venda, cessão ou outra transferência do negócio a que os Dados Pessoais digam respeito; ou</li>
    <li>Qualquer outra finalidade legítima prevista na LPDP.</li>
</ul>

<h4>Subcontratação</h4>
<p>
    A {{ Setting::get('company_name') }}, pode comunicar os Dados Pessoais a entidades por si subcontratadas para efeitos de tratamento
    dos mesmos, por conta e sob as instruções da {{ Setting::get('company_name') }}, enquanto entidade Responsável.
    <br/>
    A {{ Setting::get('company_name') }}, só contratará subcontratantes que ofereçam garantias suficientes em relação às medidas
    de segurança técnica e de organização do Tratamento de Dados Pessoais a efectuar, devendo zelar pelo cumprimento
    dessas medidas. Tais Subcontratantes estarão sujeitos ao cumprimento integral das regras previstas na Política de
    Privacidade, aquando do tratamento de Dados Pessoais por conta e sob instruções da {{ Setting::get('company_name') }}
</p>


<h4>Interconexão de Dados Pessoais</h4>
<p>
    A {{ Setting::get('company_name') }}, não levará a cabo qualquer operação de interconexão de Dados Pessoais sem a prévia 
    autorização da CNPD para o efeito.
    <br/>
    As operações de interconexão de Dados Pessoais levadas a cabo pela {{ Setting::get('company_name') }}, obedecerão aos seguintes requisitos:
</p>
<ul>
    <li>(i) Adequação à prossecução das finalidades legais ou estatutárias e de interesses legítimos daquela;</li>
    <li>(ii) Não-discriminação ou diminuição dos direitos, liberdades e garantias dos titulares dos dados,</li>
    <li>(iii) Utilização de adequadas medidas de segurança; e</li>
    <li>(iv) Ter em conta o tipo de dados objecto de interconexão.</li>
</ul>


<h4>Direitos dos Titulares dos Dados</h4>
<p>
    Nos termos previstos na LPDP, é garantido aos Titulares dos Dados o direito de acesso, actualização e rectificação
    dos seus Dados Pessoais, bem como o direito de oposição ao tratamento dos mesmos, nos termos pelo próprio declarado,
    devendo tal exercício ser efectuado presencialmente pelo titular do dado.
</p>

