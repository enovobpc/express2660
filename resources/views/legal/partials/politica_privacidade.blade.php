<h2 class="m-t-0">Política de Privacidade</h2>
<div class="spacer-15"></div>
<p>
    A {{ Setting::get('company_name') }}, pretende assegurar que o tratamento de dados pessoais, obedecendo às regras estabelecidas no
    Regulamento (UE) 2016/679 de 27 de Abril de 2016. Para este efeito foi aprovado o presente documento, o qual visa
    consagrar a Política de Privacidade da empresa, estabelecendo as principais regras observadas pela {{ Setting::get('company_name') }},
    no que diz respeito ao tratamento de dados pessoais.
    <br/>
    A recolha e tratamento dos dados pessoais fornecidos à {{ Setting::get('company_name') }} assim como o exercício dos direitos
    dos seus titulares relativamente a estes dados, regem-se por esta Política de Privacidade, pelos regulamentos internos
    e pelos restantes instrumentos utilizados pela {{ Setting::get('company_name') }}, nos termos e na medida em que sejam aplicáveis aos titulares dos dados.
</p>
<div>
    <h4>Definições sobre o tratamento de dados:</h4>
    <p>
        <ul style="list-style: none">
            <li>(a) “CNPD”: Comissão Nacional de Protecção de Dados;</li>
            <li>(b) “Consentimento do titular dos dados”: qualquer manifestação de vontade livre, específica e informada,
                nos termos da qual o titular aceita que os seus Dados Pessoais sejam objecto de tratamento;</li>
            <li>(c) “Dados Pessoais”: qualquer informação, de qualquer natureza e independentemente do respectivo suporte,
                incluindo som e imagem, relativa a uma pessoa singular identificada ou identificável; é considerada identificável
                a pessoa que possa ser identificada directa ou indirectamente, designadamente por referência a um número de
                identificação ou a um ou mais elementos específicos da sua identidade física, fisiológica, psíquica,
                económica, cultural ou social;
            </li>
            <li>(d) “Dados Sensíveis”: Dados Pessoais referentes a convicções filosóficas ou políticas, filiação
                sindical, fé religiosa, vida privada e origem racial ou étnica, bem como o tratamento de dados relativos
                à saúde e à vida sexual, incluindo os dados genéticos;
            </li>
            <li>(e) “Interconexão de dados”: forma de tratamento que consiste no relacionamento dos dados de um
                ficheiro com os dados de outro ficheiro mantido por outro responsável, ou os dados de um ficheiro
                mantido pelo mesmo responsável com uma finalidade diferente;
            </li>
            <li>(f) “Legislação relativa à protecção de Dados Pessoais - LPDP”: Regulamento (UE) 2016/679 de 27 de Abril de 2016;</li>
            <li>(g) “RPDP”: Regulamento (UE) 2016/679 de 27 de Abril de 2016;</li>
            <li>(h) “Responsável”: a (designação da Sociedade) ou outra empresa do Grupo que determine as finalidades
                e os meios de tratamento dos Dados Pessoais;</li>
            <li>(i) “Subcontratante”: a pessoa singular ou colectiva que trate os Dados Pessoais em nome e por conta do Responsável;</li>
            <li>(j) “Terceiro”: a pessoa singular ou colectiva, a autoridade pública, o serviço ou qualquer outro
                organismo a quem o Responsável transmita Dados Pessoais;</li>
            <li>(k) “Titular dos Dados”: a pessoa singular identificada ou identificável cujos Dados Pessoais são
                tratados pela {{ Setting::get('company_name') }}, ou por outro Responsável;</li>
            <li>“Tratamento de Dados Pessoais”: qualquer operação ou conjunto de operações sobre Dados Pessoais,
                efectuadas com ou sem meios automatizados, tais como a recolha, o registo, a organização, a conservação,
                a adaptação ou alteração, a recuperação, a consulta, a utilização, a comunicação por transmissão,
                por difusão ou por qualquer outra forma de colocação à disposição, com comparação ou interconexão,
                bem como o bloqueio, apagamento ou destruição.
            </li>
        </ul>
    </p>
</div>
